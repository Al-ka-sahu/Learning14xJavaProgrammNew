package ex_04_Operators;

public class Lab049_All_Operators {

    public static void main(String[] args) {


        int a = 10;
        int b = 20;

        System.out.println(a+b);
        System.out.println(a>b); // o/p will be boolean true or false
        System.out.println(a>=b); // Relational Operators
        System.out.println(a==b);
        System.out.println(a!=b);

        //Arithmetic operator +,-,%,/,*

        //Compound assignment Operators

        int age = 10;
        age +=10; // age = age+10;
        age -=10; // age = age-10;
        System.out.println(age);

//        o/p = 20
        //Bitwise operators - Not important for Automation

        // ~  - bitwise
        //>> right shift,
        //<< left shift,
        //^- XOR


    }
}
