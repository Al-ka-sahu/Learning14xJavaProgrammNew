package ex_04_Operators;

public class Lab041_Logical_Operators {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a); //  not a ka, o/p = true

        boolean b = true;
        System.out.println(!!b); // not b ka not = o/p = false

        boolean c = true || false;  // OR gate system = o/p = false, search on google
        System.out.println(c);

        boolean c1 = false && true;
        System.out.println(c1);  // AND GATE system = o/p = true

        // refer notebook notes also


    }
}
